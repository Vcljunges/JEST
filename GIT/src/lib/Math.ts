export class Math {
    sum(a: number, b: number) {
        return a + b;
    }

    Multi(a: number, b: number){
    return a * b;
    }

    sub(a: number, b: number){
        return a - b;
    }

    div(a: number, b: number){
        return a / b;
        //return b === 0 ? false : a / b;
    }
}
